WORD CRC16(BYTE *pData, BYTE nData) reentrant
{
	BYTE nCRCHi;
	BYTE nCRCLo;
#if OS_CRITICAL_METHOD == 3  
	OS_CPU_SR  cpu_sr = 0;
#endif

	//进入到SFR Page 0x0F
	OS_ENTER_CRITICAL();
	SFRPAGE = 0x0F;
	CRC0CN = 0x14;
	
	//初始化CRC
	CRC0CN |= 0x08;

	while(nData--)
	{
		CRC0IN = *pData++;
	}

	nCRCLo = CRC0DAT;
	nCRCHi = CRC0DAT;
														 
	//恢复到SFR Page 0x00
	SFRPAGE = 0x00;
	OS_EXIT_CRITICAL();

	return (nCRCHi << 8 | nCRCLo) ;
}

